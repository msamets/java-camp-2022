package dayOneAssignment;

public class Video11 {

	
	public static void main(String[] args) {
		
		for (int i = 0; i < 10; i++) {
			System.out.println(i+1);
		}
		
		System.out.println("**************");
		int i = 1;
		while (i<=10) {
			System.out.println(i);
			i++;
		}
		
		i = 1;
		System.out.println("**************");
		do {
			System.out.println(i);
			i++;
		} while (i<=10);
		
		
	}
	
}
